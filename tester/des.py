def expand(block, M):
    return [block[x-1] for x in M]

def xor(x, y):
    res = int(x, 2)^int(y,2)
    res = bin(res)[2:].zfill(16)
    return res

def hex_to_binary(hexs, bits):
    scale = 16
    num_bits = bits
    return bin(int(hexs, scale))[2:].zfill(num_bits)

def string_to_bit_array(text):#Convert a string into a list of bits
    ret = ""
    for x in text:
        a = format(ord(x), 'b')
        b = a + ""
        ret += b.zfill(8)
    return ret

